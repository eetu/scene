// Bundle the JIT recompiler (layout + emit + decode + interp + coreblock) plus
// install-src.js into ONE classic script `ejs-jit.js`, embedded into the core as
// --post-js so it self-installs Module.ejsJitGet on the emulation thread (works
// for both threaded and non-threaded cores). No external bundler needed — our
// modules only use `export const/function` and `import * as NS` / `import {…}`.
//
//   node bundle.mjs   →   writes ./ejs-jit.js
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const JIT = join(HERE, "../../jit");
const SPIKE = join(HERE, "../../spike");

// split `decl` on top-level commas (depth 0 w.r.t. () [] {}), return leading idents
function declNames(decl) {
  const out = [];
  let depth = 0,
    start = 0;
  for (let i = 0; i <= decl.length; i++) {
    const c = decl[i];
    if (c === "(" || c === "[" || c === "{") depth++;
    else if (c === ")" || c === "]" || c === "}") depth--;
    if (i === decl.length || (c === "," && depth === 0)) {
      const m = /^\s*([A-Za-z_$][\w$]*)/.exec(decl.slice(start, i));
      if (m) out.push(m[1]);
      start = i + 1;
    }
  }
  return out;
}
function load(path) {
  const raw = readFileSync(path, "utf8");
  const names = [];
  let m;
  const fn = /export\s+(?:function|class)\s+([A-Za-z0-9_$]+)/g;
  while ((m = fn.exec(raw))) names.push(m[1]);
  // const/let/var may declare MULTIPLE names: `export const X = 1, N = 8, …;`
  const cv = /export\s+(?:const|let|var)\s+([\s\S]*?);/g;
  while ((m = cv.exec(raw))) declNames(m[1]).forEach((n) => names.push(n));
  const body = raw
    .replace(/^\s*import[^\n]*\n/gm, "") // strip import lines
    .replace(/export\s+(const|let|var|function|class)/g, "$1"); // strip export keyword
  return { body, names };
}
// wrap a module body in an IIFE returning its exports, with dependency params
function iife({ body, names }, params, args) {
  return `(function(${params}){\n${body}\nreturn {${names.join(", ")}};\n})(${args})`;
}

const layout = load(join(JIT, "layout.mjs"));
const emit = load(join(SPIKE, "emit.mjs"));
const decode = load(join(JIT, "decode.mjs"));
const interp = load(join(JIT, "interp.mjs"));
const coreblock = load(join(JIT, "coreblock.mjs"));
const install = readFileSync(join(HERE, "install-src.js"), "utf8");

const out = `// GENERATED by core/ejs-jit/bundle.mjs — do not edit. 68k→WASM JIT, self-installs
// Module.ejsJitGet inside the core (post-js).
(function () {
const L = ${iife(layout, "", "")};
const w = ${iife(emit, "", "")};
const D = ${iife(decode, "", "")};
const I = ${iife(interp, "L, blockAt, isMem", "L, D.blockAt, D.isMem")};
const CB = ${iife(coreblock, "w, isMem, L", "w, D.isMem, L")};
${install}
})();
`;

const dest = join(HERE, "ejs-jit.js");
writeFileSync(dest, out);
console.log(
  `wrote ${dest} (${out.length} bytes; exports L:${layout.names.length} w:${emit.names.length} D:${decode.names.length} I:${interp.names.length} CB:${coreblock.names.length})`,
);
